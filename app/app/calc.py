"""
Calculator module for performing basic arithmetic operations.
"""


def add(x, y):
    """
    Add two numbers.

    Args:
        x (float): The first number.
        y (float): The second number.

    Returns:
        float: The sum of x and y.
    """
    return x + y


def subtract(x, y):
    """
    Subtract two numbers.

    Args:
        x (float): The first number.
        y (float): The second number.
    Returns:
        float: The difference of x and y.
    """
    return y - x
